CREATE SCHEMA LA-POC-RAW;
create or replace TABLE POC_DB.LA_POC_RAW.ADDRESS (
	ADDRESS_ID NUMBER(38,0) NOT NULL,
	ADDRESS_TYPE VARCHAR(25),
	ADDRESS VARCHAR(100),
	IS_VALID NUMBER(38,0),
	CREATED_TIMESTAMP TIMESTAMP_NTZ(9),
	UPDATED_TIMESTAMP TIMESTAMP_NTZ(9),
	STATE VARCHAR(50),
	primary key (ADDRESS_ID)
);